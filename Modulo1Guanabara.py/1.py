n = input('Insira algo: ')
print ('É alfabetico {}! '.format(n.isalpha()))
print ('É decimal  {}! '.format(n.isdecimal()))
print ('É digitado {}! '.format(n.isdigit()))
print ('É identificadp {}! '.format(n.isidentifier()))
print ('É Minusculo {}! '.format(n.islower()))
print ('É Titulo {}! '.format(n.istitle()))
print ('É Maiusculo {}! '.format(n.isupper()))
print ('É alfanumerico {}! '.format(n.isalnum()))